# შენი დავალებაა რომ ტერმინალში გამოიტანო სიის თითუეული ელემენტი ცალ ცალკე ,გამოიყენე for და while ციკლები 

names = ["Nika", "Giorgi", "Ana", "Luka", "Mari"]

for i in range(len(names)):
    print(names[i])

i = 0
while i < len(names):
    print(names[i])
    i += 1
